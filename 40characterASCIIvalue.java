/* Write a Java program to print the ascii value of a given character.
Expected Output
The ASCII value of Z is :90
*/
public class Solution {
public static void main(String[] args) {
char chOne = 'a';
char chTwo = 'b';
char chThree = 'Z';
int charASCII1 = chOne;
int charASCII2 = chTwo;
int charASCII3 = chThree;
System.out.println(charASCII1);
System.out.println(charASCII2);
System.out.println(charASCII3);
}
}
