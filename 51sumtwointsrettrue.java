/*Write a Java program to calculate the sum of two integers and return true if the sum is equal
to a third integer.
Sample Output:
Input the first number : 5
Input the second number: 10
Input the third number : 15
The result is: true
*/
import java.util.Scanner;
class Main {
public static void main(String[] args) {
Scanner newObject = new Scanner(System.in);
int integerOne = newObject.nextInt();
int integerTwo = newObject.nextInt();
int integerThree = newObject.nextInt();
int integerSum = integerOne + integerTwo;
if (integerSum == integerThree) {
System.out.println("true");
}
else {
System.out.println("false");
}
newObject.close();
}
}
